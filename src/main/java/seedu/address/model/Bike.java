package seedu.address.model;

/**
 * Represents a Bike in the address book.
 */
public class Bike {

    private String name;

    public Bike(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
